function [B] = getMinors(A)
    B(1) = A(1,1)*A(2,2) - A(2,1)*A(1,2);
    B(2) = A(1,1)*A(3,2) - A(3,1)*A(1,2);
    B(3) = A(1,1)*A(4,2) - A(4,1)*A(1,2);
    B(4) = A(2,1)*A(3,2) - A(3,1)*A(2,2);
    B(5) = A(2,1)*A(4,2) - A(4,1)*A(2,2);
    B(6) = A(3,1)*A(4,2) - A(4,1)*A(3,2);
    B = B';
end